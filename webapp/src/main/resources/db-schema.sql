
    create table ACCOUNT (
        ACCOUNT_ID bigint generated by default as identity (start with 1),
        UPDATED_ON_DATE timestamp,
        VERIFIED boolean default '0',
        ADMIN_ID bigint,
        SERVICE_PLAN_ID bigint,
        primary key (ACCOUNT_ID)
    );
    create table ADMIN (
        USER_ID bigint not null,
        primary key (USER_ID)
    );
    create table CONTACT_INFO (
        CONTACT_ID bigint generated by default as identity (start with 1),
        UPDATED_ON_DATE timestamp,
        EMAIL varchar(255),
        EMAIL_VERIFIED boolean,
        PHONE_VERIFIED boolean,
        PRIMARY_PHONE varchar(255),
        SECONDARY_PHONE varchar(255),
        primary key (CONTACT_ID)
    );
    create table CORPORATION (
        CORP_ID bigint generated by default as identity (start with 1),
        UPDATED_ON_DATE timestamp,
        DOMAIN varchar(255),
        NAME varchar(255),
        ADMIN bigint,
        CONTACT_ID bigint,
        SERVICE_PLAN_ID bigint,
        primary key (CORP_ID)
    );
    create table CORP_ACCOUNT (
        CORP_MAIL_ID varchar(255) not null,
