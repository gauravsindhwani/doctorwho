    );
    create table DOCTOR_SPECIALITY (
        DOCTOR_ID bigint not null,
        SPECIALITY varchar(255) not null,
        primary key (DOCTOR_ID, SPECIALITY)
    );
    create table MEDICAL_INFORMATION (
        USER_ID bigint not null,
        UPDATED_ON_DATE timestamp,
        ALLERGIES varchar(255),
        DOB date,
        GENDER varchar(255),
        WEIGHT smallint,
        primary key (USER_ID)
    );
    create table PATIENT (
        USER_ID bigint not null,
        ACCOUNT_ID bigint,
        primary key (USER_ID)
    );
    create table POST (
        ID bigint generated by default as identity (start with 1),
        UPDATED_ON_DATE timestamp,
        TEXT longvarchar,
        PARENT_ID bigint,
        POST_BY varchar(255),
        QUERY_ID bigint,
        USER_ID bigint,
        primary key (ID)
    );
    create table QUERY (
        ID bigint generated by default as identity (start with 1),
        UPDATED_ON_DATE timestamp,
        DOCTOR_ID bigint,
        PATIENT_ID bigint,
        primary key (ID)
    );
    create table ROLE (
        ROLE_NAME varchar(255) not null,
        primary key (ROLE_NAME)
    );
    create table SERVICE_PLAN (
        SERVICE_PLAN_ID bigint generated by default as identity (start with 1),
        UPDATED_ON_DATE timestamp,
        DURATION varchar(255),
        FEE integer,
        NAME varchar(255),
        DEPENDENTS smallint,
        primary key (SERVICE_PLAN_ID)
    );
    create table SPECIALITIES (
        SPECIALITY varchar(255) not null,
        primary key (SPECIALITY)
    );
    create table USER (
        USER_ID bigint generated by default as identity (start with 1),
        UPDATED_ON_DATE timestamp,
        FIRST_NAME varchar(255),
        LAST_NAME varchar(255),
        LOGIN_NAME varchar(255),
        LOGIN_TYPE varchar(255),
        PASSWORD varchar(255),
        CONTACT_ID bigint,
        ROLE_NAME varchar(255),
        primary key (USER_ID)
    );
